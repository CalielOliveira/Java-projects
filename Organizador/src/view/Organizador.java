    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        final JFileChooser fc = new JFileChooser();
        FileFilter filter = new FileNameExtensionFilter("png e jpg","jpg", "png");
        fc.addChoosableFileFilter(filter);
        fc.setMultiSelectionEnabled(true);
               
        int returnVal = fc.showOpenDialog(Organizador.this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File[] files = fc.getSelectedFiles();  
            
            for(File f: files){
                try {
                    dados.add(new Arquivo(f));                    
                } catch (IOException ex) {
                    Logger.getLogger(Organizador.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            attDados(dados);
        } else {
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        OrganizadorDAO.excluirDados();
        
        model.removeAllElements();
        dados.clear();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        VetorOrdenado vetor = new VetorOrdenado(dados);
        
        long startTime = System.currentTimeMillis();
        vetor.Ordenar();
        long endTime = System.currentTimeMillis();
        
        dados = vetor.getDados();
        attDados(dados);
        
        jLabel4.setText(String.valueOf((endTime - startTime) / 1000));
        
    }//GEN-LAST:event_jButton4ActionPerformed

